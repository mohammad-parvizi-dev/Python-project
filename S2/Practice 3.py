# صورت سوال:
# دو رشته به نام Str1 و Str2 از کاربر گرفته می‌شود.
# سپس با استفاده از یک تابع به نام ch_word بررسی می‌شود که آیا این دو رشته با یکدیگر برابر هستند یا خیر.
# در این بررسی، دو حالت وجود دارد:

# حالت حساس: در این حالت، به بزرگی و کوچکی حروف توجه می‌شود.
# حالت غیرحساس: در این حالت، به بزرگی و کوچکی حروف توجه نمی‌شود و هر دو رشته به حروف کوچک تبدیل می‌شوند.
# بر اساس این دو حالت، تابع ch_word خروجی‌های زیر را نمایش می‌دهد:

# اگر دو رشته در هر دو حالت (حساس و غیرحساس) برابر باشند، عبارت "ok" چاپ می‌شود.
# اگر دو رشته فقط در حالت غیرحساس برابر باشند، عبارت "ok" چاپ می‌شود.
# در غیر این صورت، عبارت "error" چاپ می‌شود.



#توضیحات بیشتر در این لینک
