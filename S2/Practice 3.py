Str1 = input("string1: ")  # دریافت رشته اول از کاربر
Str2 = input("string2: ")  # دریافت رشته دوم از کاربر

def ch_word(w1, w2, d=False):  # تعریف تابع `ch_word` با سه پارامتر:
    # `w1` و `w2` رشته‌های ورودی، و `d` یک مقدار پیش‌فرض `False` (برای حالت غیرحساس)

    if d == True:  # بررسی حالت حساس (d = True)
        if w1 == w2:  # مقایسه دو رشته با در نظر گرفتن بزرگی و کوچکی حروف
            print("ok")  # چاپ "ok" در صورت برابری کامل
        else:
            print("error")  # چاپ "error" در صورت عدم برابری کامل

    elif d == False:  # بررسی حالت غیرحساس (d = False)
        w1 = w1.lower()  # تبدیل هر دو رشته به حروف کوچک
        w2 = w2.lower()

        if w1 == w2:  # مقایسه دو رشته با حروف کوچک
            print("ok")  # چاپ "ok" در صورت برابری با حروف کوچک
        else:
            print("error")  # چاپ "error" در صورت عدم برابری با حروف کوچک

ch_word(Str1, Str2)  # اجرای تابع `ch_word` با رشته‌های ورودی Str1 و Str2

