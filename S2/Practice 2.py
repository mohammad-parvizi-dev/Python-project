Temp1 = 0  # متغیری برای ذخیره اولین عدد کوچک‌تر
Temp2 = 0  # متغیری برای ذخیره دومین عدد کوچک‌تر

Data = input("add new int: ")  # خواندن اولین عدد از کاربر

while Data != "end":  # حلقه while تا زمانی که کاربر "end" را وارد کند
    if int(Data) < Temp1:  # بررسی اینکه آیا عدد ورودی جدید کوچکتر از Temp1 است یا خیر
        Temp2 = Temp1  # جابجایی مقادیر Temp1 و Temp2
        Temp1 = int(Data)  # ذخیره عدد جدید به عنوان Temp1
    elif int(Data) < Temp2:  # بررسی اینکه آیا عدد ورودی جدید کوچکتر از Temp2 است یا خیر
        Temp2 = int(Data)  # ذخیره عدد جدید به عنوان Temp2
    Data = input("add new int and type 'end' to calc: ")  # خواندن عدد بعدی از کاربر

