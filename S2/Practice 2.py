# یافتن دومین عدد کوچک‌تر در میان اعداد ورودی توسط کاربر
# در این برنامه، از کاربر خواسته می‌شود تا اعداد صحیح را به صورت جداگانه وارد کند.
# برنامه تا زمانی که کاربر عبارت "end" را وارد کند، به خواندن اعداد ادامه می‌دهد.
# در طول این فرآیند، برنامه دو متغیر Temp1 و Temp2 را برای ذخیره اولین و دومین عدد کوچک‌تر  نگهداری می‌کند.
# در نهایت، برنامه عدد ذخیره شده در متغیر Temp2 را که دومین عدد کوچک‌تر  می‌باشد، چاپ می‌کند.



