Num1 = int(input("Enter number 1: "))  # دریافت عدد اول از کاربر و تبدیل آن به عدد صحیح
Num2 = int(input("Enter number 2: "))  # دریافت عدد دوم از کاربر و تبدیل آن به عدد صحیح
Operator = input("Enter operator: ")  # دریافت عملگر از کاربر (مثلا +، -، *، /)
result = 0  # متغیری برای ذخیره نتیجه

if Operator == "+":  # بررسی اگر عملگر برابر با "+" باشد
    result = Num1 + Num2  # جمع دو عدد و ذخیره نتیجه در متغیر result
elif Operator == "-":  # بررسی اگر عملگر برابر با "-" باشد
    result = Num1 - Num2  # تفریق دو عدد و ذخیره نتیجه در متغیر result
elif Operator == "*":  # بررسی اگر عملگر برابر با "*" باشد
    result = Num1 * Num2  # ضرب دو عدد و ذخیره نتیجه در متغیر result
elif Operator == "/":  # بررسی اگر عملگر برابر با "/" باشد
    if Num2 > Num1:  # بررسی اگر عدد دوم بزرگتر از عدد اول باشد
        print("Error")  # چاپ پیام خطا
        Num1 = int(input("Enter number 1: "))  # دریافت مجدد عدد اول از کاربر
        Num2 = int(input("Enter number 2: "))  # دریافت مجدد عدد دوم از کاربر
    result = Num1 / Num2  # تقسیم دو عدد و ذخیره نتیجه در متغیر result

print(result)  # چاپ نتیجه نهایی

