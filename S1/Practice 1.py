# از توابع int و float برای تبدیل ورودی کاربر به نوع داده صحیح یا اعشاری در صورت نیاز استفاده کنید.
# در صورت بروز خطا (تقسیم بر صفر یا تقسیم عدد کوچکتر بر عدد بزرگتر)، پیام خطای مناسب را نمایش دهید.
